# ## Kurzeinführung in Git
#
# - (Verteiltes) Versionskontrollsystem
# - Ermöglicht:
#   - Verfolgen von Änderungen an Dateien
#   - Wiederherstellen von älteren Versionen
#   - Zusammenführen von Änderungen
#   - Gemeinsame Arbeit an Projekten
# - Verwendet für Softwareentwicklung, Kursunterlagen, ...

