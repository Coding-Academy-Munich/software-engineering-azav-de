# ## Warum ist OOAD/OOP ein guter Ansatz?
#
# - Code:
#   - Struktur/Modularität
#   - Wiederverwendung
# - Design:
#   - "Intuitive" Modellierung von Systemen/Problemen
#   - **Kontrolle von Abhängigkeiten** (Dependency Inversion)
# - Kombination:
#   - **Niedrige Repräsentationslücke**


