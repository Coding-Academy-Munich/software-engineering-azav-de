# ## Was ist ein Unit-Test?
#
# - Testet einen kleinen Teil des Codes (eine "Unit")
# - Hat kurze Laufzeit
# - Ist isoliert

# %% [markdown]
#
# ## Wozu dienen Unit-Tests?
#
# - Nach Testquadranten:
#   - Unterstützen das Team bei der Entwicklung
#   - Sind auf die Technik fokussiert
# - Nach Regression/Rückmeldung Trade-Off:
#   - Sind auf schnelles Feedback fokussiert
#   - Jeder Test findet relativ wenige Fehler
# - Werden beim Schreiben von Code sehr häufig ausgeführt

# %% [markdown]
#
# ## Was können Unit Tests testen?
#
# - Funktionen
# - Methoden
# - (Klassen)
# - (Einfache Komponenten)
