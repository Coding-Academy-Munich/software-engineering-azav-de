# ## Wichtige Git Befehle (Lokal)
#
# - `git init`: Repository initialisieren
# - `git status`: Dateistatus überprüfen
# - `git add`: Dateien zum Staging-Bereich hinzufügen
# - `git commit`: Änderungen ins Repository übernehmen
# - `git log`: Commit-Historie anzeigen
# - `git diff`: Unterschiede anzeigen

# %% [markdown]
#
# ## Wichtige Git Befehle (Branch-Management)
#
# - `git branch`: Branches verwalten
# - `git switch`: Branch wechseln
# - `git merge`: Branches zusammenführen
# - `git rebase`: Branches linearisieren

# %% [markdown]
#
# ## Wichtige Git Befehle (Remote)
#
# - `git clone`: Remote-Repository klonen
# - `git remote`: Remote-Verbindungen verwalten
# - `git fetch`, `git pull`: Änderungen vom Remote holen

# %% [markdown]
#
# ## Git in VS Code
#
# - VS Code bietet integrierte Git-Unterstützung
# - Alle wichtigen Git-Befehle können direkt in VS Code ausgeführt werden
# - Visualisierung von Änderungen und Historie
# - Konfliktlösung und Branch-Management

